// sometimes we want our program to execute few set of instructions over and over again , for ex: printing 1 to 100 , first 100 even numbers etc.

// Hence Loops make it easy for a programmer to tell computer taht a given set of instruction must be executed repeatedly .


#include <stdio.h>

int main() {

    printf("Hello") ; 
    printf("World \n") ;

    int a = 1 ;
    printf("%d\n" , a); 
    a++ ; 
    printf("%d\n" , a); 
    a++ ; 
    printf("%d\n" , a); 
    a++ ; 
    printf("%d\n" , a); 
    a++ ; 
    printf("%d\n" , a); 
    a++ ; 
    printf("%d\n" , a); 
    a++ ; 

    // soo let take an if we have to write 1 to 600 ,, we cannot write like this thats why we use loop


    return 0 ;
}

