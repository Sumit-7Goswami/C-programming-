// write a program to print multiplication table of 10 in reverse order 

/* we will do this any of the loop method , now we do with while loop*/

#include <stdio.h>

int main()
{
    for (int i = 10; i ; i--)
    {
        printf("10 x %d = %d\n" , i , 10*i ) ; 
    }
    
    return 0 ;
}



