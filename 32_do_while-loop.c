#include <stdio.h>

int main(){
    int i = 0 ;

    do{
        printf("The value of i is %d\n" , i ) ;
        i++ ;
    }while(i < 10) ;
    return 0 ;

    
}

/*do-while-loop works very similiar to while loop .

while --> checks the condition & then executes the code
do-while  ---> executes the code & then checks the condition*/